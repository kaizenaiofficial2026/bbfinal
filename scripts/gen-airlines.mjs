import airlineCodes from "airline-codes";
import fs from "node:fs";

const models = airlineCodes.models || [];
const BAD = new Set(["", "\\N", "-", "N/A"]);
const seen = new Set();
const rows = [];
for (const m of models) {
  const a = m.attributes || m;
  const name = String(a.name || "").trim();
  const iata = String(a.iata || "").trim().toUpperCase();
  const country = String(a.country || "").trim();
  if (a.active !== "Y") continue;
  if (BAD.has(name) || BAD.has(iata) || iata.length !== 2) continue;
  const key = `${name}|${iata}`;
  if (seen.has(key)) continue;
  seen.add(key);
  rows.push({ name, iata, country: BAD.has(country) ? "" : country });
}
rows.sort((a, b) => a.name.localeCompare(b.name, "en"));

const header = `// AUTO-GENERATED by scripts/gen-airlines.mjs — do not edit by hand.
// Active airlines (name + IATA + country) from airline-codes (OpenFlights),
// bundled statically so the airline picker searches client-side with no API.
export type Airline = { name: string; iata: string; country: string };

export const AIRLINES: readonly Airline[] = ${JSON.stringify(rows, null, 0)} as const;
`;
fs.writeFileSync("lib/data/airlines.ts", header);
console.log(`wrote lib/data/airlines.ts with ${rows.length} airlines`);
console.log("samples:", rows.filter(r => ["EK","UL","QR","BA","AI"].includes(r.iata)).map(r => `${r.iata}=${r.name}`).join(", "));
