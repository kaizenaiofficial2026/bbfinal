import { Country } from "country-state-city";
import fs from "node:fs";
const flagDir = "public/flags";
const haveFlag = new Set(
  fs.readdirSync(flagDir).filter((f) => f.endsWith(".svg")).map((f) => f.replace(".svg", "")),
);
const all = Country.getAllCountries();
const rows = all
  .filter((c) => haveFlag.has(c.isoCode.toLowerCase()))
  .map((c) => ({ code: c.isoCode.toUpperCase(), name: c.name }))
  .sort((a, b) => a.name.localeCompare(b.name, "en"));
const header = `// AUTO-GENERATED by scripts/gen-countries.mjs — do not edit by hand.
// Source: country-state-city (ISO 3166-1) intersected with the flag SVGs in
// public/flags. \`name\` is the canonical English name (stored value + search
// fallback); the UI shows a locale-aware name via Intl.DisplayNames at runtime.
export type Country = { code: string; name: string };

export const COUNTRIES: readonly Country[] = ${JSON.stringify(rows, null, 2)} as const;

const BY_CODE = new Map(COUNTRIES.map((c) => [c.code, c]));
export function countryByCode(code: string): Country | undefined {
  return BY_CODE.get(code.toUpperCase());
}
export function isCountryCode(code: string): boolean {
  return BY_CODE.has(code.toUpperCase());
}
`;
fs.writeFileSync("lib/data/countries.ts", header);
console.log(`wrote lib/data/countries.ts with ${rows.length} countries`);
console.log("samples:", rows.filter((r) => ["LK","IN","US","AE"].includes(r.code)).map((r)=>`${r.code}=${r.name}`).join(", "));
